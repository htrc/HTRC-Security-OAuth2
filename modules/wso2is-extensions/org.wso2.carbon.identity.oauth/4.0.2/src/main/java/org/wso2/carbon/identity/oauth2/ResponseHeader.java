package org.wso2.carbon.identity.oauth2;

public class ResponseHeader {

    private String key;
    private String value;
    
    public String getKey() {
        return key;
    }

    public void setKey(String key) {
        this.key = key;
    }

    public String getValue() {
        return value;
    }

    public void setValue(String value) {
        this.value = value;
    }
}
